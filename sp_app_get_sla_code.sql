create or alter procedure dbo.sp_app_get_sla_code @season_id int as
	select l.sla_code, 
		   r.in_season_sla, 
		   r.off_season_sla
	from dbo.fn_sla_code_valid(@season_id) as l
	left join
		 sladb.dbo.vw_sla_code_pivot as r
	on l.sla_code = r.sla_code
