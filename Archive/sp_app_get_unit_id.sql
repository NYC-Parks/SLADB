create or alter procedure dbo.sp_app_get_unit_id @sector nvarchar(50) as 
	select r.unit_id,
		   concat(r.unit_id, ' | ', r.unit_desc) as unit_desc
	from (select *
		  from [dataparks].dailytasks.dbo.ref_sector_districts
		  where active = 1 and
		        sector = @sector) as l
	inner join
		sladb.dbo.tbl_ref_unit as r
	on l.district = r.unit_mrc
	where lower(unit_status) != 'd';
