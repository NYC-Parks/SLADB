create or alter procedure dbo.sp_u_tbl_sla_season as
	begin
		begin transaction
			update sladb.dbo.tbl_sla_season
				set effective = 0
			where effective = 1 and 
				  cast(dateadd(hour, -1, effective_end) as date) = cast(getdate() as date);
		commit;
	end;
